    // Добавление товара в избранное

    ADD_TO_CARD_WISHLIST(state, productId) {
      const existingProductWishlist = state.wishlistProducts.find(
        (item) => item.id === productId.id
      );
      if (existingProductWishlist) {
        const removeFromWishlist = state.wishlistProducts.findIndex(
          (item) => item.id === productId.id
        );
        state.wishlistProducts.splice(removeFromWishlist, 1);
      } else {
        state.wishlistProducts.push(productId);
      }
    },

    // Добавление товара в корзину
    ADD_TO_CART(state, productId) {
      // const existingProduct = state.cartItems.find((item) => item.id === productId.id);

      const existingProduct = state.cartItems.find(
        (item) => item.id === productId.id && item.sizeIdx === productId.sizeIdx
      );
      if (existingProduct) {
        existingProduct.quantity += 1;
      } else if (productId.sizeIdx) {
        state.cartItems.push(productId);
      }
      this.dispatch('saveCart');
    },
    // УДАЛЕНИЕ ТОВАРА ИЗ КОРЗИНЫ
    REMOVE_FROM_BASKET(state, index) {
      state.cartItems[index].quantity = 1;
      state.cartItems.splice(index, 1);
      this.dispatch('saveCart');
    },
    // изменение количества товаров в корзине
    INCREASE(state, index) {
      state.cartItems[index].quantity += 1;
      this.dispatch('saveCart');
    },
    DECREMENT(state, index) {
      if (state.cartItems[index].quantity > 1) {
        state.cartItems[index].quantity -= 1;
      } else {
        state.cartItems.splice(index, 1);
      }
      this.dispatch('saveCart');
    },

    // Изменение кол-ва товаров на PDP
    INCREASE_PRODUCT_PDP(state, itemProductPDP) {
      state.cartItems.filter((elem) => {
        if (elem.id === itemProductPDP.id) {
          elem.quantity += 1;
          localStorage.removeItem('cartItems');
          this.dispatch('saveCart');
          return elem.quantity;
        }
        return elem.quantity;
      });
    },

    DECREMENT_PRODUCT_PDP(state, itemProductPDP) {
      state.cartItems.findIndex((elem, index) => {
        if (elem.id === itemProductPDP.id && elem.quantity > 1) {
          elem.quantity -= 1;
          localStorage.removeItem('cartItems');
          this.dispatch('saveCart');
          return elem.quantity;
        }
        return state.cartItems.splice(index, 1);
      });
    },

